/// Available <see cref="LinkLabel"/> behaviors.
/// </summary>
public enum LinkLabelBehavior
{
    /// <summary>
    /// Default.
    /// </summary>
    SystemDefault = 0,

    /// <summary>
    /// Always underline.
    /// </summary>
    AlwaysUnderline = 1,

    /// <summary>
    /// Hover underline.
    /// </summary>
    HoverUnderline = 2,

    /// <summary>
    /// Never underline.
    /// </summary>
    NeverUnderline = 3
}
